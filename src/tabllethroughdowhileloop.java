public class tabllethroughdowhileloop {

        public static void main(String[] args) {
            int number = 5; // Change this to print the table of any number
            int i = 1;
            do {
                System.out.println(number + " * " + i + " = " + (number * i));
                i++;
            } while (i <= 10);
        }


}
